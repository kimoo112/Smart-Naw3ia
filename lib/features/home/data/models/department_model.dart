class DepartmentModel {
  final String nameEn;
  final String nameAr;
  final String descriptionEn;
  final String descriptionAr;
  final List<String> objectivesEn;
  final List<String> objectivesAr;
  final List<String> coursesEn;
  final List<String> coursesAr;
  final StaffMember head;
  final IconData icon;
  final List<StaffMember> facultyMembers;
  final String visionEn;
  final String visionAr;
  final String image;
  final String missionEn;
  final String missionAr;

  const DepartmentModel({
    required this.nameEn,
    required this.icon,
    required this.image,
    required this.nameAr,
    required this.descriptionEn,
    required this.descriptionAr,
    required this.objectivesEn,
    required this.objectivesAr,
    required this.coursesEn,
    required this.coursesAr,
    required this.head,
    required this.facultyMembers,
    required this.visionEn,
    required this.visionAr,
    required this.missionEn,
    required this.missionAr,
  });

  String getName(String locale) => locale == 'ar' ? nameAr : nameEn;
  String getDescription(String locale) =>
      locale == 'ar' ? descriptionAr : descriptionEn;
  List<String> getObjectives(String locale) =>
      locale == 'ar' ? objectivesAr : objectivesEn;
  List<String> getCourses(String locale) =>
      locale == 'ar' ? coursesAr : coursesEn;
  String getVision(String locale) => locale == 'ar' ? visionAr : visionEn;
  String getMission(String locale) => locale == 'ar' ? missionAr : missionEn;

  AcademicStaff getOrganizedStaff() {
    // Filter staff members by department and remove duplicates
    final departmentStaff = facultyMembers.where((member) {
      // If department is not specified, include in current department
      if (member.department == null) return true;
      // Otherwise, only include if department matches
      return member.department == nameEn;
    }).toList();

    // Remove duplicates and sort by rank
    final uniqueStaff = <String, StaffMember>{};
    var counter = 0;

    // Add head first to ensure they appear at the top of their rank category
    final headKey = head.email ?? '${head.nameEn}_head';
    uniqueStaff[headKey] = head;

    // Filter out the head from department staff if they exist there
    final filteredStaff = departmentStaff.where((member) {
      if (member.email != null && head.email != null) {
        return member.email != head.email;
      }
      return member.nameEn != head.nameEn;
    }).toList();

    // Add remaining staff members
    for (var member in filteredStaff) {
      final key = member.email ?? '${member.nameEn}_${counter++}';
      if (uniqueStaff.containsKey(key)) {
        final existing = uniqueStaff[key]!;
        if (member.getRank() > existing.getRank()) {
          uniqueStaff[key] = member;
        }
      } else {
        uniqueStaff[key] = member;
      }
    }

    // Convert to list and sort by rank
    final staffList = uniqueStaff.values.toList();
    staffList.sort((a, b) {
      // If ranks are equal and one is the head, put head first
      if (a.getRank() == b.getRank()) {
        if (a == head) return -1;
        if (b == head) return 1;
      }
      return b.getRank().compareTo(a.getRank());
    });

    return AcademicStaff.fromList(staffList);
  }

  String getFormattedStaff(String locale) {
    return getOrganizedStaff().getFormattedStaff(locale);
  }
}

